    private void CerrarSesiónActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CerrarSesiónActionPerformed
        // TODO add your handling code here:
        
         if (hayespacio1(getListaReportes())){
            ListaReportes= redimensionarArre(getListaReportes());
        
        for (int i = 0; i <= getListaReportes().length; i++) {
            
            if(getListaReportes()[i] == null){
                if(Bv.getListaVirtual()[i].getTipo() != "Libro Virtual"){
            
            ListaReportes = redimensionarArre(getListaReportes());
             ListaReportes[i] = new DatosReportes(Bv.getListaVirtual()[i].getUsuario(), Bv.getListaVirtual()[i].getAutor(), Bv.getListaVirtual()[i].getTitulo(), Bv.getListaVirtual()[i].getAño(), Bv.getListaVirtual()[i].getPalabrasClave(), Bv.getListaVirtual()[i].getDescripcion(), Bv.getListaVirtual()[i].getTemas(), Bv.getListaVirtual()[i].getEdicion(), Bv.getListaVirtual()[i].getArea(), Bv.getListaVirtual()[i].getCopias(), Bv.getListaVirtual()[i].getCategoria(), Bv.getListaVirtual()[i].getDisponibles(), Bv.getListaVirtual()[i].getISBN(), Bv.getListaVirtual()[i].getTipo());
            
             
             PantallaPrincipal newframe = new PantallaPrincipal();

        newframe.setVisible(true);

        this.dispose();
            }
                else{
                
                PantallaPrincipal newframe = new PantallaPrincipal();

        newframe.setVisible(true);

        this.dispose();
                
                }
            }
            
        }
        
        
        
         }
         
    }//GEN-LAST:event_CerrarSesiónActionPerformed

    private void CargarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CargarDatosActionPerformed
        // TODO add your handling code here:
        
        String[][] matriz = new String[Cu.getListaUsuarios().length][5]; 
            
        for (int i = 0; i < Cu.getListaUsuarios().length; i++) {
            if(Cu.getListaUsuarios()[i] != null) { 
                
                matriz[i][1]=Cu.getListaUsuarios()[i].getID();
                matriz[i][2]= Cu.getListaUsuarios()[i].getNombre();
                matriz[i][3]= Cu.getListaUsuarios()[i].getApellido();
                matriz[i][4]="" + Cu.getListaUsuarios()[i].getUsuario();
                
                
                if(matriz[i][4].equals(TxtUsuario.getText())) {
                    Txt_ID.setText(matriz[i][1]); 
                    Txt_Nombre.setText(matriz[i][2]);
                Txt_Apellido.setText(matriz[i][3]);
                
                
                

                }
            }
            
        }
        
    }//GEN-LAST:event_CargarDatosActionPerformed

