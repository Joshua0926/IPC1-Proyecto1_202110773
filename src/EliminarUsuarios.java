    private void RefrescarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RefrescarActionPerformed
        // TODO add your handling code here:
        String[][] matriz = new String [Cu.getListaUsuarios().length][100];
        for (int i = 0; i < Cu.getListaUsuarios().length; i++) {
            if(Cu.getListaUsuarios()[i] != null){

                matriz[i][0]= Cu.getListaUsuarios()[i].getID();
                matriz[i][1]= Cu.getListaUsuarios()[i].getNombre();
                matriz[i][2]= Cu.getListaUsuarios()[i].getApellido();
                matriz[i][3]= Cu.getListaUsuarios()[i].getUsuario();
                matriz[i][4]= Cu.getListaUsuarios()[i].getRol();
                matriz[i][5]= Cu.getListaUsuarios()[i].getPassword();

            }
        }

        String[] encabezado = {"ID", "Nombre", "Apellido", "Usuario", "Rol", "Contrase単a"};
        TablaUsuarios.setModel(new DefaultTableModel(
            matriz,
            encabezado
        ));

    }//GEN-LAST:event_RefrescarActionPerformed

    private void BuscarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarUsuarioActionPerformed
        // TODO add your handling code here:
        
        
        
        String[][] matriz = new String[Cu.getListaUsuarios().length][7]; 
            
        for (int i = 0; i < Cu.getListaUsuarios().length; i++) {
            if(Cu.getListaUsuarios()[i] != null) { 
                
                matriz[i][1]="" + Cu.getListaUsuarios()[i].getID();
                matriz[i][2]= Cu.getListaUsuarios()[i].getNombre();
                matriz[i][3]= Cu.getListaUsuarios()[i].getApellido();
                matriz[i][4]= Cu.getListaUsuarios()[i].getUsuario();
                matriz[i][5]= Cu.getListaUsuarios()[i].getRol();
                matriz[i][6]= Cu.getListaUsuarios()[i].getPassword();
                
                if(matriz[i][1].equals(Txt_ID.getText())) {
                     
                    Txt_Nombre.setText(matriz[i][2]);
                Txt_Apellido.setText(matriz[i][3]);
                Txt_Usuario.setText(matriz[i][4]);
                Txt_Rol.setText(matriz[i][5]);
                Txt_Contrase単a.setText(matriz[i][6]);
                

                }
            }
            
        }
    }//GEN-LAST:event_BuscarUsuarioActionPerformed

