    DatosUsuarios[] redimensionarArreglo(DatosUsuarios[] arreglo){
        DatosUsuarios[] temporal = new DatosUsuarios[arreglo.length + 1];
        for (int i = 0; i < arreglo.length; i++) {
            temporal[i] = arreglo[i];
        }
        return temporal;
    }
    
    Boolean hayEspacio(DatosUsuarios[] arreglo){
        Boolean espacio = false;
        for (int i = 0; i < arreglo.length; i++) {
            if(arreglo[i] == null){
                espacio=true;
                break; 
            }
        }
        return espacio;
    }

