    private void CrearUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearUsuarioActionPerformed
        // TODO add your handling code here:
        
 
        if(hayEspacio(getListaUsuarios())){
           ListaUsuarios = redimensionarArreglo(getListaUsuarios());
        }
        
        for (int i = 0; i < getListaUsuarios().length; i++) {
            if(getListaUsuarios()[i] == null){
                
               
             
            ListaUsuarios[i] = new DatosUsuarios(ID.getText(), Nombre.getText(), Apellido.getText(), Usuario.getText(), Rol.getText(), Password.getText());
            break; 
               
            
            }
        }
        
        UsuariosCreados newframe = new UsuariosCreados();
        
        newframe.setVisible(true);
        
        this.dispose();
        
        
    }//GEN-LAST:event_CrearUsuarioActionPerformed

