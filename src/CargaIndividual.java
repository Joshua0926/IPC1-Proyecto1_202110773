    private void CrearBiblioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearBiblioActionPerformed
        // TODO add your handling code here:
        
        String tipo = Txt_Tipo.getSelectedItem().toString();

         if(hayespacio(getListaBiblios())){
            setListaBiblios(redimensionarArr(getListaBiblios()));
        }
        
        for (int i = 0; i < getListaBiblios().length; i++) {
            if(getListaBiblios()[i] == null){
                
                JOptionPane.showMessageDialog(null, "El Lbro se ha creado con Exito");
                setListaBiblios(redimensionarArr(getListaBiblios())); 
            ListaBiblios[i] = new DatosBiblios(Txt_Autor.getText(), Txt_Titulo.getText(), Txt_A単o.getText(), Txt_PalabrasClave.getText(), Txt_Descripcion.getText(), Txt_Temas.getText(), Txt_Edicion.getText(), Txt_Area.getText(), Txt_Copias.getText(), Txt_Categoria.getText(), Txt_Disponibles.getText(), Txt_Isbn.getText(), tipo);
            break; 
            
            
            }
        }
        
        BibliosCreadas newframe = new BibliosCreadas();
        
        newframe.setVisible(true);
        
        this.dispose();

        

    }//GEN-LAST:event_CrearBiblioActionPerformed

